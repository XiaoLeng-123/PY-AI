def search_stock_by_code_local(code):
    """从本地数据库查询股票信息"""
    stock_info = STOCK_DATABASE.get(code)
    if stock_info:
        return {
            'code': code,
            'name': stock_info['name'],
            'market': stock_info['market']
        }
    return None


def search_stock_by_name_local(name):
    """从本地数据库根据名称查询股票信息"""
    for code, info in STOCK_DATABASE.items():
        if name in info['name'] or info['name'] in name:
            return {
                'code': code,
                'name': info['name'],
                'market': info['market']
            }
    return None
